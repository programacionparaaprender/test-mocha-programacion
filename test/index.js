describe('Sumas', ()=> {
    it('Suma de + con + tiene que ser +', ()=>{
        console.log(2 + 2);
        console.log(4);
    });

    it('Suma de + y mayor que el segundo elemento que es - = +', ()=>{
        console.log(3 + (-2));
        console.log(1);
    });
});
